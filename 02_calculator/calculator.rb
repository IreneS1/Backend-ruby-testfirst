# add method that takes two parameters and adds them
def add(num1, num2)
    num1 + num2
end

# subtract method that takes two parameters and subtracts the second from first
def subtract(num1, num2)
    num1 - num2
end

# sum method takes an array as a parameter and adds all numbers in array to get sum
def sum(arry)
    arry.sum
end


